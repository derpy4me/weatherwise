export function meteoToIcon(code, time = "day") {
  return wmoMapping[code][time];
}

export function owmToIcon(code) {
  const wmo = owmToWmoMap[code];
  let time;
  if (code.toLowerCase().includes("n")) {
    time = "night";
  } else {
    time = "day";
  }
  return wmoMapping[wmo][time];
}
